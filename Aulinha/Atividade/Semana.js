let opcao = 50

switch (opcao) {
    case 1:
        console.log('Domingo - Final de semana') 
        break;
    case 2:
        console.log('Segunda-feira - Dia de semana') 
        break;
    case 3:
        console.log('Terça-feira - Dia de semana') 
        break;
    case 4:
        console.log('Quarta-feira - Dia de semana') 
        break;
    case 5:
        console.log('Quinta-feira - Dia de semana') 
        break;
    case 6:
        console.log('Sexta-feira - Final de semana') 
        break;
    case 7:
        console.log('Sábado - Final de semana') 
        break;
        
    default:
        console.log('Opção inválida.') 
        break;

}